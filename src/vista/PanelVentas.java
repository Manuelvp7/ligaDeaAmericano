    public void cargarComboCategoria(List<Categoria> categorias){
        
        comboCategoria.removeAllItems();
        for (int i = 0; i < categorias.size(); i++) {
            
            comboCategoria.addItem(categorias.get(i).getCategoria());
            
        }
        
    }
    
        public void cargarComboProveedores(List<Proveedor> proveedores){
        
        comboProveedor.removeAllItems();
        
        for (int i = 0; i < proveedores.size(); i++) {
            
            
            comboProveedor.addItem(proveedores.get(i).getNombre());
            
        }
        
    }
        
    public boolean busquedaEnTablaArticulos(Object[] articulo){
        
                
        for (int j = 0; j < tablaArticulos.getRowCount(); j++) {
            
            if(articulo[0]==tablaArticulos.getValueAt(j, 0)&&articulo[1]==tablaArticulos.getValueAt(j, 1))
                return false;
            
                        
        }
        return true;
    }
    
    public void calcularImporte(){
        
                
        float importe = 0;
        int i = tablaArticulos.getSelectedRow();            

        
        
        try {
            
            
            System.out.println("JAJAJAJAJAJA");
            for (int j = 0; j < tablaArticulos.getRowCount(); j++) {
                
                int noPiezas = 0;
                
                if(tablaArticulos.getValueAt(j,3).getClass()==String.class){
                    noPiezas = Integer.parseInt((String) tablaArticulos.getValueAt(j,3));
                }else
                    noPiezas = (int)tablaArticulos.getValueAt(j,3);
                
                if(noPiezas>(int)tablaArticulos.getValueAt(j, 4))
                   tablaArticulos.setValueAt(tablaArticulos.getValueAt(j, 4), j, 3);
                importe += noPiezas* (float)tablaArticulos.getValueAt(j, 2);
                
            }
        
        lblImporte.setText(String.valueOf(importe));
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "INGRESE UNA CANTIDAD");
        }
        
    }
    
    

