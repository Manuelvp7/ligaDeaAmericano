    private void btnRegistrarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarVentaActionPerformed

        
        calcularImporte();
       
        
        for (int i = 0; i < tablaArticulos.getRowCount() ; i++) {
            
            System.out.println(tablaArticulos.getValueAt(i,3).getClass());
            
            int noPiezas = 0;
            if(tablaArticulos.getValueAt(i,3).getClass()==String.class){
            
                noPiezas = Integer.parseInt((String) tablaArticulos.getValueAt(i,3));
            }else
                noPiezas = (int)tablaArticulos.getValueAt(i,3);
            
            
            unaInterfazDeVentas.registrarDetalleDeVenta(lblTienda.getText(),
                    Integer.parseInt(lblIdVenta.getText()),
                    Integer.parseInt(lblAnio.getText()),Integer.parseInt(lblTrimestre.getText()),(String)tablaArticulos.getValueAt(i,0),
                    (String)tablaArticulos.getValueAt(i,1),
                    noPiezas);
            
            unaInterfazDeVentas.actualizarExistencias(lblTienda.getText(), (String)tablaArticulos.getValueAt(i,0),
                    (String)tablaArticulos.getValueAt(i,1),noPiezas);
           
        }
        unaInterfazDeVentas.registrarEmpleadoTieneVentas(lblCURP.getText(), Integer.parseInt(lblIdVenta.getText()),
                Integer.parseInt(lblAnio.getText()),Integer.parseInt(lblTrimestre.getText()));
        
        
        unaInterfazDeVentas.registrarRecordDeVentas((String)lblTienda.getText(), Integer.parseInt(lblIdVenta.getText()),Integer.parseInt(lblAnio.getText()),
                Integer.parseInt(lblTrimestre.getText()), Double.parseDouble(lblImporte.getText()));
        
        
        
        modeloTablaDeBusqueda.setRowCount(0);
        modeloTablaDeCompra.setRowCount(0);
        
               
        lblIdVenta.setText( String.valueOf(unaInterfazDeVentas.calcularIdVenta(lblTienda.getText(), Integer.parseInt(lblAnio.getText()),
                Integer.parseInt(lblTrimestre.getText()))) ); 
        
    }//GEN-LAST:event_btnRegistrarVentaActionPerformed

